function productList(category, presymb, symb){
	let title = document.createElement('h1');
	title.innerHTML = category;
	symb = symb||'«»';
	symb = symb=='none'?['','']:symb;
	document.getElementById('product-list').appendChild(title.cloneNode(true));
	for (let i in DataBase[category]){
		let e = document.querySelector('.product').cloneNode(true);
		e.className = 'product';
		dElem = DataBase[category][i];
		e.querySelector('.product-title h2').innerHTML = presymb + ' ' + symb[0] + dElem.title + symb[1];
		try{
			e.querySelector('.product-description span').innerHTML = dElem.description.substring(0,128)+'...';
		} catch {
			e.querySelector('.product-description span').innerHTML = '';
		}
		e.querySelector('.product-cost h2').innerHTML = dElem.cost;
		e.querySelector('.product-img img').src = dElem.imgsrc;
		e.querySelector('.buy-button').setAttribute('category', category);
		e.querySelector('.buy-button').setAttribute('num', i);
		document.getElementById('product-list').appendChild(e.cloneNode(true));
	}
}
