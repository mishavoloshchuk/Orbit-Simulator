function ordersList(){
	document.getElementById('ordersBox').innerHTML = '';
	let isEmpty = true;
	for (let i in orderList){
		let e = document.querySelector('.orderItem').cloneNode(true);
		e.className = 'orderItem';
		dElem = DataBase[orderList[i][0]][orderList[i][1]];
		e.querySelector('.orderTitle').innerHTML = dElem.title;
		e.querySelector('.ordCost').innerHTML = dElem.cost * orderList[i][2];
		e.querySelector('.ordImg').src = dElem.imgsrc;
		e.querySelector('.numbBox').value = orderList[i][2];
		e.querySelector('.deleteOrder').setAttribute('ordid', i);
		e.querySelector('.numbBox').setAttribute('ordid', i);
		e.querySelector('.innerOrderItem').setAttribute('ordid', i);
		document.getElementById('ordersBox').appendChild(e.cloneNode(true));
		isEmpty = false;
	}
	if (!isEmpty){
		document.querySelector('.totalCost').style.display = 'initial';
		document.querySelector('.ordersEmpty').style.display = 'none';
		document.querySelector('.ordersTotalCost').innerHTML = getTotalCost();
		document.querySelector('.ordersFreeDelivery').innerHTML = 300 - getTotalCost()<=0?'Безкоштовна':'50 грн (До безкоштовної: ' + (300 - getTotalCost()) + 'грн)';
		document.querySelector('.ordersTotalCostWithDelivery').innerHTML = 300 - getTotalCost()<=0?getTotalCost():getTotalCost()+50;
	} else {
		document.querySelector('.ordersEmpty').style.display = 'initial';
		document.querySelector('.totalCost').style.display = 'none';
	}
}
function buyButtonPress(event){
	let add = false;
	for (let i in orderList){
		if (isEqual(orderList[i], event.params)){
			orderList[i][2]++;
			add = true;
			break;
		}
	}
	if (!add){
		orderList[orderList.length] = event.params;
		orderList[orderList.length-1][2] = 1;
	}
	document.querySelector('.order-count-icon').innerHTML = addArrIndex(orderList, 2);
	saveOrders();
}
function getTotalCost(){
	let totalCost = 0;
	for (let i in orderList){
		dElem = DataBase[orderList[i][0]][orderList[i][1]];
		totalCost += dElem.cost*orderList[i][2];
	}
	return totalCost;
}
function saveOrders(){
	window.sessionStorage.setItem('order', JSON.stringify(orderList));
}
function ordersButton(){
	if (document.querySelector('.orders').className == 'orders display-none'){
		document.querySelector('.orders').className = 'orders ordersPosition';
		document.querySelector('.tint').className = 'tint';
		document.querySelector('body').style.overflowY = 'hidden';
		document.querySelector('body').style.marginLeft = '-9px';
		ordersList();
	} else {
		document.querySelector('.orders').className = 'orders display-none';
		document.querySelector('.tint').className = 'tint display-none';
		document.querySelector('body').style.overflowY = 'auto';
		document.querySelector('body').style.marginLeft = '0';
	}
}
function valueOrderInput(el){
	let id = +el.getAttribute('ordid');
	if (+el.value < 0 ){
		el.value = 1;
	}
	orderList[id][2] = Math.abs(+el.value);
	dElem = DataBase[orderList[id][0]][orderList[id][1]];
	saveOrders();
	if (document.querySelector('.order-count-icon')){
		document.querySelector('.order-count-icon').innerHTML = addArrIndex(orderList, 2);
	}
	el.parentNode.nextSibling.innerHTML = dElem.cost * Math.abs(+el.value);
	document.querySelector('.ordersTotalCost').innerHTML = getTotalCost();
	document.querySelector('.ordersFreeDelivery').innerHTML = 300 - getTotalCost()<=0?'Безкоштовна':'50 грн (До безкоштовної: ' + (300 - getTotalCost()) + 'грн)';
	document.querySelector('.ordersTotalCostWithDelivery').innerHTML = 300 - getTotalCost()<=0?getTotalCost():getTotalCost()+50;
}
function deleteOrder(ord){
	orderList.splice(+ord, 1);
	saveOrders();
	if (document.querySelector('.order-count-icon')){
		document.querySelector('.order-count-icon').innerHTML = addArrIndex(orderList, 2);	
	}
	document.getElementById('ordersBox').innerHTML = '';
	ordersList();
}
